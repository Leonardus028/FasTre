package com.example.fastre.ui.authentication;

public class User {

    public String fullName, age, bloodType, email;


    public User() {

    }

    public User(String fullName, String age, String bloodType, String email) {
        this.fullName = fullName;
        this.age = age;
        this.bloodType = bloodType;
        this.email = email;
    }

}

